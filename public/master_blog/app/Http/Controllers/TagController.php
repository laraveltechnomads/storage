    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    { 
        if ($request->ajax())
        {       
           $tag = Tag::where('tag', $request->tagnew)->first();     
           if(!$tag)
           {
                $tag = new Tag; 
                $tag->tag = $request->tagnew;
                $tag->save();
                return response()
                ->json(['status' => '200', 'message' => 'Tag Created!', 'data' => $tag])
                ->withCallback($request->input('callback'));
           }
           return response()
            ->json(['status' => '404', 'message' => 'Tag Already Exist!'])
            ->withCallback($request->input('callback'));
        }
    }

